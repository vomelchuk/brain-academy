package module2.oop2_1;

public class Person {
	//fields
	private String name;
	private int age;
	private long taxID;
	public String getName() {
		return name;
	}
	//methods
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public long getTaxID() {
		return taxID;
	}
	public void setTaxID(long taxID) {
		this.taxID = taxID;
	}
	
	//methods

}
