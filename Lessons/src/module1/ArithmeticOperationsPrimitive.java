package module1;
public class ArithmeticOperationsPrimitive {

	public static void main(String[] args) {
		int a = 20;
		int b = 11;

		System.out.println("a = " + a + "\nb = " + b);
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a % b = " + (a % b));
	}
}
