package labs;

public class LabWork_1_4_4 {

	public static void main(String[] args) {

		int a = 5;

		System.out.println("prefix increment `" + a + "` = " + ++a);
		System.out.println("prefix decrement `" + a + "` = " + --a);
		System.out.println("postfix increment `" + a + "` = " + a++);
		System.out.println("pretfix decrement `" + a + "` = " + a--);
	}

}
